package bzu.skyn.travehelper.tools;

public class Scence {
	private String sname; //景点名
	private String sprovence;//省名
	private String scity;//城市名
	private String sintro;//景点介绍
	
	public String getsname() {
		return sname;
	}
	public void setsname(String sname) {
		this.sname = sname;
	}
	public String getsprovence() {
		return sprovence;
	}
	public void setsprovence(String sprovence) {
		this.sprovence = sprovence;
	}
	public String getscity() {
		return scity;
	}
	public void setscity(String scity) {
		this.scity = scity;
	}
	public String getsintro() {
		return sintro;
	}
	public void setsintro(String sintro) {
		this.sintro = sintro;
	}
}
